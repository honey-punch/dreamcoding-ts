{
  /**
   * Print Loading State
   */
  type LoadingState = {
    state: 'loading';
  };

  type SuccessState = {
    state: 'success';
    response: {
      body: string;
    };
  };

  type FailState = {
    state: 'fail';
    reason: string;
  };

  type ResourceLoadState = LoadingState | SuccessState | FailState;

  function printState(state: ResourceLoadState) {
    switch (state.state) {
      case 'loading':
        console.log('ðŸ‘€ loading...');
        break;
      case 'success':
        console.log(`ðŸ˜ƒ ${state.response.body}`);
        break;
      case 'fail':
        console.log(`ðŸ˜± ${state.reason}`);
        break;
      default:
        throw new Error('no state');
    }
  }

  printState({ state: 'loading' }); // ðŸ‘€ loading...
  printState({ state: 'success', response: { body: 'loaded' } }); // ðŸ˜ƒ loaded
  printState({ state: 'fail', reason: 'no network' }); // ðŸ˜± no network
}
